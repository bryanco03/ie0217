#include <iostream>
using namespace std;

int main(){

    // Se asigna un valor entero a num_int
    int num_int = 9;

    // Se declara una variable de tipo double
    double num_double;

    // Conversion implicita
    // Se asigna un valor entero a una variable tipo double

    num_double = num_int;


    cout << "num_int = " << num_int << endl;
    cout << "num_double = " << num_double << endl;

    return 0;
    
}